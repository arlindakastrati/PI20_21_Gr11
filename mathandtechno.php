       <h2>Personalized STEM lessons using technology</h2>
       <p style="width: 730px;">   Increased access to technology for STEM allows for a more customized learning experience.
             Because no two learners are exactly alike, technology can provide individual students
             with content and supports that are particularly helpful to their individual needs.
             Kids can view lessons, tutorials, screencasts, and other instructional media on their
             own device and at their own pace. So if one student is still confused on a topic, 
             and another is ready for additional challenges, technology can enable each to take 
             the appropriate next step.</p>
       
