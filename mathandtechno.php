        <p style="width: 730px; margin-top: -150px;">A great example of how technology empowers learners is the phenomenon of
          <strong><a href="https://www.khanacademy.org/" target="_blank">Khan Academy</a></strong>
         Sal Khan did not intend to build a non-profit educational organization when 
        he started posting the recordings of his math lessons on YouTube (as he 
        delightfully explains in his TED Talk). He was only trying to help tutor
        his cousins from afar and didn’t see any reason to set the videos to 
        Private mode. From the feedback from his cousins, and then from other
        people from all over the world who found his videos, he realized how 
        valuable this medium was and the importance of being able to choose,
        rewind, and control the lessons.</p>
        

        <p>The Khan Academy platform has emerged from his work, giving learners personalized 
        learning experiences in a number of ways. For example, users can take quizzes to see 
        what concepts they have mastered and what they need more practice with. Or students 
        that learn better through written text can access transcripts that accompany the 
        instructional videos. Providing the ability to direct and control learning pathways
         is a powerful reason to include technology in our own math instruction.</p>
